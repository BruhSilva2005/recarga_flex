<h3 id="recarga">4. RECARGA do CARTÃO</h3>
<p>Para poder recargar o cartão no sistema precisa seguir os seguintes passo:<br>
1-No menu inicial localize o botaõ de recarga(Parte Superior), Click em "Recarregar".<br>
    2-Aberto o Menu de usuario, click em "Recarregar".<br>
    3-Em tela de Recarregar, Preencher as informações pedida.<br>
    4-Apos preenchimento, Click em pagar.<br>
    4-Apos o pagamento, Click em "voltar ao inicio".<br>
    5-Visualizar Mensagem de "Pagamento efetuado com Sucesso".<br>
    5.1 caso não consiga, click no botão de ajuda
    5.2-Refazer passo a passo.
